#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		ft_putchar('-');
		ft_putnbr(-(nb / 10));
		ft_putchar('8');
		return ;
	}
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putchar('0' + (nb % 10));
		nb = nb / 10;
	}
	else
	{
		if ((nb >= 0) && (nb <= 9))
		{
			ft_putchar('0' + nb);
		}
	}
}
